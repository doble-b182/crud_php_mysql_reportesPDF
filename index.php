    <h2>Generadoras Centrales</h2>
<p>En general, la generación de energía eléctrica consiste en transformar alguna clase de energía (química,
   cinética, térmica, lumínica, nuclear, solar entre otras), en energía eléctrica. Para la generación industrial
    se recurre a instalaciones denominadas centrales eléctricas, que ejecutan alguna de las transformaciones
    citadas. Estas constituyen el primer escalón del sistema de suministro eléctrico. La generación eléctrica se realiza,
     básicamente, mediante un generador eléctrico; si bien estos no difieren entre sí en cuanto a su principio
     de funcionamiento, varían en función a la forma en que se accionan.</p>
     <p>
      Dependiendo de la fuente primaria de energía utilizada, las centrales generadoras se clasifican en
      químicas cuando se utilizan plantas de radioactividad, que generan energía eléctrica con el contacto de
      esta, termoeléctricas (de carbón, petróleo, gas, nucleares y solares termoeléctricas), hidroeléctricas
      (aprovechando las corrientes de los ríos o del mar: mareomotrices), eólicas y solares fotovoltaicas.
      La mayor parte de la energía eléctrica generada a nivel mundial proviene de los dos primeros tipos de
      centrales reseñados. Todas estas centrales, excepto las fotovoltaicas, tienen en común el elemento generador,
      constituido por un alternador de corriente, movido mediante una turbina que será distinta dependiendo del tipo de energía primaria utilizada.
     </p>
