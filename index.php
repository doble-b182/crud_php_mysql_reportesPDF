<h2>Unidades generadoras</h2>
<p>Un generador eléctrico es todo dispositivo capaz de mantener una diferencia de potencial eléctrica entre dos de sus puntos (llamados polos, terminales o bornes) transformando la energía mecánica en eléctrica. Esta transformación se consigue por la acción de un campo magnético sobre los conductores eléctricos dispuestos sobre una armadura (denominada también estátor). Si se produce mecánicamente un movimiento relativo entre los conductores y el campo, se generará una fuerza electromotriz (F.E.M.). Este sistema está basado en la ley de Faraday.<br>
Aunque la corriente generada es corriente alterna, puede ser rectificada para obtener una corriente continua.
En el diagrama adjunto se observa la corriente inducida en un generador simple de una sola fase. La mayoría de
los generadores de corriente alterna son de tres fases. <br>
El proceso inverso sería el realizado por un motor eléctrico, que transforma energía eléctrica en mecánica.</p>
